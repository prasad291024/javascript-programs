/* JavaScript Conditional Statement: if...else
-------------------------------------------
 Executes one block if the condition is true, another if false.

// Syntax:
if (condition) {
  // Code if condition is true
} else {
  // Code if condition is false
}

*/


// Example:
const score = 45;

if (score >= 50) {
  console.log("You passed!");
} else {
  console.log("You failed.");
}
// Output: You failed.