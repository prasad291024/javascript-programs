// Object Creation in JavaScript
// There are several ways to create objects in JavaScript. The most common methods are using object literals and the Object constructor.
// Using object literal
const car = {
  brand: "Toyota",
  model: "Camry",
  year: 2022
};

// Using Object constructor
const bike = new Object();
bike.brand = "Honda";
bike.model = "Shine";
bike.year = 2021;
console.log(car);
console.log(bike);