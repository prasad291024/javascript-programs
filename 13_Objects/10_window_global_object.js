


// Window Global Object (Browser Only)
// The window object is the global object in a browser environment.
// It represents the browser's window and provides methods and properties to interact with it.
// Here are some common properties and methods of the window object:
// Note: This code should be run in a browser environment to see the effects.
// Example usage of the window object:

var globalVar = "Accessible via window object";

console.log(window.globalVar); // "Accessible via window object"
console.log(window.location.href); // Logs the current URL of the page
console.log(window.innerWidth); // Logs the inner width of the window
console.log(window.innerHeight); // Logs the inner height of the window
console.log(window.navigator.userAgent); // Logs the user agent string
console.log(window.document.title); // Logs the title of the document
console.log(window.screen.width); // Logs the width of the screen
console.log(window.screen.height); // Logs the height of the screen
console.log(window.history.length); // Logs the number of entries in the session history
